//Retorna true si es valido el mail
function validateEmail(email) {
  var re = /^(?:[a-z0-9!#$%&amp;'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#$%&amp;'*+/=?^_`{|}~-]+)*|"(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21\x23-\x5b\x5d-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])*")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\[(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?|[a-z0-9-]*[a-z0-9]:(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21-\x5a\x53-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])+)\])$/;
  return re.test(email);
}


$(function () {

  let validateField = (e) => {

    let dataInput = inputMail.val()

    let flag = false

    if (validateEmail(dataInput)) {
      flag = true
    }

    //Usamos el flag para agregar/sacar clase al elemento input
    if (flag) {
      inputMail.addClass('is-valid').removeClass('is-invalid')
    } else {
      inputMail.addClass('is-invalid').removeClass('is-valid')
    }
  }


  var inputMail = $('#email')
  inputMail.keyup(validateField)

})